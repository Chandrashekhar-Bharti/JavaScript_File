const month = "march"

switch (month) {   // if month  is not match the cases then is  going to execute the code inside of the default statement.
    case "jan":
        console.log("January");
        break;
    case "feb":
        console.log("feb");
        break;
    case "march":
        console.log("march");
        break;
    case "april":
        console.log("april");
        break;

    default:
        console.log("default case match");
        break;
}